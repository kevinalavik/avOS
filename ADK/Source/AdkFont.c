static const U8 *AdkFontLookup(char Ch)
{
	static const U8 Blank[8] = { 0, 0, 0, 0, 0, 0, 0, 0 };
	static const U8 Space[8] = { 0, 0, 0, 0, 0, 0, 0, 0 };
	static const U8 Dash[8] = { 0, 0, 0, 0x7E, 0, 0, 0, 0 };
	static const U8 Underscore[8] = { 0, 0, 0, 0, 0, 0, 0x7E, 0 };
	static const U8 Slash[8] = {
		0x06, 0x0C, 0x18, 0x30, 0x60, 0x40, 0x00, 0x00
	};
	static const U8 Colon[8] = { 0, 0x18, 0x18, 0, 0x18, 0x18, 0, 0 };
	static const U8 Dot[8] = { 0, 0, 0, 0, 0, 0x18, 0x18, 0 };
	static const U8 Comma[8] = { 0, 0, 0, 0, 0x18, 0x18, 0x30, 0 };
	static const U8 OpenParen[8] = {
		0x0C, 0x18, 0x30, 0x30, 0x30, 0x18, 0x0C, 0
	};
	static const U8 CloseParen[8] = { 0x30, 0x18, 0x0C, 0x0C,
									  0x0C, 0x18, 0x30, 0 };
	static const U8 OpenBracket[8] = { 0x1E, 0x18, 0x18, 0x18,
									   0x18, 0x18, 0x1E, 0 };
	static const U8 CloseBracket[8] = { 0x78, 0x18, 0x18, 0x18,
										0x18, 0x18, 0x78, 0 };
	static const U8 Greater[8] = {
		0x40, 0x60, 0x30, 0x18, 0x30, 0x60, 0x40, 0
	};
	static const U8 Less[8] = { 0x02, 0x06, 0x0C, 0x18, 0x0C, 0x06, 0x02, 0 };
	static const U8 Equal[8] = { 0, 0x7E, 0, 0x7E, 0, 0, 0, 0 };
	static const U8 Plus[8] = { 0, 0x18, 0x18, 0x7E, 0x18, 0x18, 0, 0 };
	static const U8 Asterisk[8] = { 0, 0x66, 0x3C, 0xFF, 0x3C, 0x66, 0, 0 };
	static const U8 Quote[8] = { 0x18, 0x18, 0x10, 0, 0, 0, 0, 0 };
	static const U8 DoubleQuote[8] = { 0x36, 0x36, 0x24, 0, 0, 0, 0, 0 };
	static const U8 Exclaim[8] = { 0x18, 0x18, 0x18, 0x18, 0x18, 0, 0x18, 0 };
	static const U8 Question[8] = { 0x3C, 0x66, 0x06, 0x0C, 0x18, 0, 0x18, 0 };
	static const U8 Pipe[8] = { 0x18, 0x18, 0x18, 0x18, 0x18, 0x18, 0x18, 0 };

	if (Ch >= '0' && Ch <= '9')
		return AdkFontDigits[(U8)Ch - '0'];
	if (Ch >= 'A' && Ch <= 'Z')
		return AdkFontUpper[(U8)Ch - 'A'];
	if (Ch >= 'a' && Ch <= 'z')
		return AdkFontLower[(U8)Ch - 'a'];

	switch (Ch) {
	case ' ':
		return Space;
	case '-':
		return Dash;
	case '_':
		return Underscore;
	case '/':
		return Slash;
	case ':':
		return Colon;
	case '.':
		return Dot;
	case ',':
		return Comma;
	case '(':
		return OpenParen;
	case ')':
		return CloseParen;
	case '[':
		return OpenBracket;
	case ']':
		return CloseBracket;
	case '>':
		return Greater;
	case '<':
		return Less;
	case '=':
		return Equal;
	case '+':
		return Plus;
	case '*':
		return Asterisk;
	case '\'':
		return Quote;
	case '"':
		return DoubleQuote;
	case '!':
		return Exclaim;
	case '?':
		return Question;
	case '|':
		return Pipe;
	default:
		return Blank;
	}
}

void AdkFontDrawChar(AdkCanvas *Canvas, S32 X, S32 Y, char Ch, U32 Color)
{
	const U8 *Glyph = AdkFontLookup(Ch);

	for (U32 Row = 0; Row < ADK_FONT_GLYPH_HEIGHT; Row++) {
		U8 Bits = Glyph[Row];
		for (U32 Col = 0; Col < ADK_FONT_GLYPH_WIDTH; Col++) {
			if ((Bits & (0x80u >> Col)) == 0)
				continue;

			AdkRect PixelRect = { X + (S32)Col, Y + (S32)Row, 1, 1 };
			AdkCanvasFillRect(Canvas, &PixelRect, Color);
		}
	}
}

void AdkFontDrawText(AdkCanvas *Canvas, S32 X, S32 Y, const char *Text,
					 U32 Color)
{
	if (!Text)
		return;

	for (Size Index = 0; Text[Index]; Index++)
		AdkFontDrawChar(Canvas, X + (S32)(Index * ADK_FONT_GLYPH_WIDTH), Y,
						Text[Index], Color);
}
